myString = input('Write something here: ')      # Создайте переменную и присвойте ей значение строки с произвольным текстом (функция input()).
print('Input length:', len(myString))           # Выведите на экран длину строки.
print(myString.upper())                         # Выведите строку в верхнем регистре.
print(myString.lower())                         # Выведите строку в нижнем регистре.
print(myString.replace(' ', ''))    # Измените строку, удалив все пробелы.
print(myString[0])                              # Выведите первый символ строки.
print(myString[-1])                             # Выведите последний символ строки.